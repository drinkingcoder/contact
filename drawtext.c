unsigned char asc[256*16];		//提前处理的ASCII码表 
int Background_Color,Front_Color;	//背景色和前置色 

void drawhz(int x,int y,byte buf[16][2],int size)	//保证buf中的编码为中文编码，且只有一个字，在指定位置按指定大小输出 
{
   word sixteen_dots;
   int i, j,x0,y0;
   x0=x;y0=y;
   for(i=0; i<16; i++)
   {
      sixteen_dots = buf[i][0]<<8 | buf[i][1];
      for(j=0; j<16; j++)
      {
         if(sixteen_dots  &  1<<(15-j))
         {
            setfillstyle(SOLID_FILL, Front_Color);
            bar(x, y, x+size-1, y+size-1);
         }
         else
         {
            setfillstyle(SOLID_FILL, Background_Color);
            bar(x, y, x+size-1, y+size-1);
         }
         x += size;
      }
      x = x0;
      y += size;
   }
   return;
}

void drawen(int x,int y,unsigned char* buf,int size)//保证buf中的编码为英文编码，且只有一个字母，在指定位置按指定大小输出 
{
   unsigned char eight_dots;
   int i, j,x0,y0;
   x0=x;y0=y;
   for(i=0; i<16; i++)
   {
      eight_dots = buf[i];
      for(j=0; j<8; j++)
      {
         if(eight_dots  &  1<<(7-j))
         {
            setfillstyle(SOLID_FILL, Front_Color);
            bar(x, y, x+size-1, y+size-1);
         }
         else
         {
            setfillstyle(SOLID_FILL, Background_Color);
            bar(x, y, x+size-1, y+size-1);
         }
         x += size;
      }
      x = x0;
      y += size;
   }	
   return;
}

void draw_hz(int x,int y,char *buf,int size)//保证buf中的编码为中文编码，buf长度不限，在指定位置按指定大小输出 
{
	FILE* fp=fopen("hzk","rb");
	int len=2;
	int i,j,k,offset;
	byte hz[3],s[16][2];
	for(i=0;i<len;i+=2)
	{
		hz[0]=buf[i];
		hz[1]=buf[i+1];
		hz[2]='\0';
		offset = ((hz[0] - 0xA1) * 94 + (hz[1] - 0xA1)) * 0x20;
		fseek(fp,offset,SEEK_SET);
		fread(s,1,2*16,fp);
		drawhz(x,y,s,size);
		x=x+size*16;
	}
	fclose(fp);
	return;
}


void draw_en(int x,int y,char *buf,int size)//保证buf中的编码为英文编码，buf长度不限，在指定位置按指定大小输出 
{
	int len=1;
	int i,j,offset;
	unsigned char *pasc;
   	for(i=0;i<len;i++)
   	{
   		pasc=&asc[buf[i]*16];
   		drawen(x,y,pasc,size);
   		x=x+size*16;
   	}
   	return;
}

void draw_text(int x,int y,char *buf,int size)//在指定位置按指定大小中英文混合输出 
{
	int i,j,len=strlen(buf);
	for(i=0;i<len;)
	{
		if(((int)buf[i])>>7)
		{
			draw_hz(x,y,&buf[i],size);
			x+=size*16;
			i+=2;
		} else
		{
			draw_en(x,y,&buf[i],size);
			x+=size*8;
			i+=1;
		}
	}
	return;
}
